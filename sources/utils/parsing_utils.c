#include "../../includes/cub3d.h"

int	recup_nb(char *tmp, int *i)
{
	int nb;

	nb = 0;
	while (tmp[*i] >= '0' && tmp[*i] <= '9')
		nb = (nb * 10) + (tmp[(*i)++] - '0');
	return (nb);
}
